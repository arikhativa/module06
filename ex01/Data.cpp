#include "Data.hpp"

/*
** ------------------------------- CONSTRUCTOR --------------------------------
*/

Data::Data()
	: _i(55)
{
}

Data::Data( const Data & src )
	: _i(src.getValue())
{
}


/*
** -------------------------------- DESTRUCTOR --------------------------------
*/

Data::~Data()
{
}


/*
** --------------------------------- OVERLOAD ---------------------------------
*/

Data &				Data::operator=( Data const & rhs )
{
	if ( this != &rhs )
	{
		this->_i = rhs.getValue();
	}
	return *this;
}

std::ostream &			operator<<( std::ostream & o, Data const & i )
{
	o << "Data[" << i.getValue() << "]";
	return o;
}


/*
** --------------------------------- METHODS ----------------------------------
*/


/*
** --------------------------------- ACCESSOR ---------------------------------
*/

int	Data::getValue(void) const
{
	return _i;
}

